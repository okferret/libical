// libICAL Swift Wrapper
//
// 本模块为 libical C 库提供 Swift 封装，包含以下类型：
//   - Component  : iCalendar 组件（VCALENDAR、VEVENT 等）
//   - Property   : iCalendar 属性（DTSTART、SUMMARY 等）
//   - Parameter  : iCalendar 属性参数（TZID、CN 等）
//   - Time       : 日期/时间类型（icaltimetype）
//   - Duration   : 时间段类型（icaldurationtype）
//   - Period     : 时间区间类型（icalperiodtype）
//   - Timezone   : 时区类型（icaltimezone）
//   - Recurrence : 重复规则类型（icalrecurrencetype）
//   - Parser     : iCalendar 字符串解析器
//   - Enums      : 各类枚举值（ComponentKind、PropertyKind 等）
